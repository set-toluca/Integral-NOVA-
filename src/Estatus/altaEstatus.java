    private void b_guardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_guardar1ActionPerformed
        // TODO add your handling code here:
        h=new Herramientas(user, 0);
        h.session(sessionPrograma);          
        if(nombre.getText().compareTo("")!=0)
        {
            if(consultaEstatus(nombre.getText())==false)
            {
                Estatus nuevoEstatus = new Estatus();
                nuevoEstatus.setEstatusNombre(nombre.getText());
                nuevoEstatus.setDescripcion(descripcion.getText());
                String respuesta=guardarEstatus(nuevoEstatus);
                if(respuesta==null)
                    JOptionPane.showMessageDialog(null, "Error al guardar los datos");
                else
                {
                    JOptionPane.showMessageDialog(null, "Registro almacenado");
                    this.borra_cajas();
                    nombre.requestFocus();
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "¡El nombre del estatus ya existe!");
                this.borra_cajas();
                nombre.requestFocus();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "¡Debe introducir el nombre del estatus!");
            nombre.requestFocus();
        }
    }//GEN-LAST:event_b_guardar1ActionPerformed

