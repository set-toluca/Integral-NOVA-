    private List<Object[]> executeHQLQuery(String hql) {
        try {
            Session session = HibernateUtil.getSessionFactory().openSession();
            session.beginTransaction();
            Query q = session.createQuery(hql);
            List resultList = q.list();
            session.getTransaction().commit();
            session.disconnect();
            return resultList;
        }catch (HibernateException he) {
            he.printStackTrace();
            List lista= null;
            return lista;
        }
    }
    
    public boolean consultaEstatus(String nombre )
    {        
        List <Object[]> resultList=executeHQLQuery("from Estatus obj where obj.estatusNombre='"+nombre+"'");
        if(resultList.size()>0)
            return true;
        else
            return false;
    }
     
    private String guardarEstatus(Estatus obj) 
    {
        Session session = HibernateUtil.getSessionFactory().openSession();
        String Nombre = null;
        try 
        {
            session.beginTransaction();
            Nombre=(String) session.save(obj);
            session.save(obj);
            session.getTransaction().commit();
        } 
        catch (HibernateException he) 
        {
            he.printStackTrace();
            session.getTransaction().rollback();
            Nombre= null;
        }   
        finally
        {
            session.close();
        }
        return Nombre;
    }
    
