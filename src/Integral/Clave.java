    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(this.t_clave.getText().compareTo("")!=0)
        {
            Session session = HibernateUtil.getSessionFactory().openSession();
            try
            {
                session.beginTransaction().begin();
                Usuario u=(Usuario)session.get(Usuario.class, user.getIdUsuario());
                u.setClave(this.t_clave.getText());
                session.update(u);
                session.getTransaction().commit();
                JOptionPane.showMessageDialog(this, "La contraseña se ha cambiado");
                this.dispose();
            }catch(Exception e)
            {
                e.printStackTrace();
                session.getTransaction().rollback();
                JOptionPane.showMessageDialog(this, "Error el registro no se pudo guarar");
            }
            finally 
            {
                if(session.isOpen())
                    session.close(); 
            }
        }
        else
            JOptionPane.showMessageDialog(this, "La contraseña no puede ir vacia");
    }//GEN-LAST:event_jButton1ActionPerformed


