    private List<Object[]> executeHQLQuery(String hql) {
        Session session = HibernateUtil.getSessionFactory().openSession();
        try {
            session.beginTransaction();
            Query q = session.createQuery(hql);
            List resultList = q.list();
            session.getTransaction().commit();
            session.disconnect();
            return resultList;
        } catch (HibernateException he) {
            he.printStackTrace();
            List lista= null;
            return lista;
        }
        finally
        {
            if(session.isOpen())
            session.close();
        }
    }
    
    private void buscaDato()
    {
        String consulta="from Tipo";
        List <Object[]> resultList=executeHQLQuery(consulta);
        if(resultList.size()>0)
        {
            t_datos.setModel(ModeloTablaReporte(resultList.size(), columnas));
            int i=0;
            for (Object o : resultList)
            {
                Tipo actor = (Tipo) o;
                model.setValueAt(actor.getTipoNombre(), i, 0); 
                if(actor.getEPesado()==0)
                    model.setValueAt(false, i, 1);
                else
                    model.setValueAt(true, i, 1);
                i++;
            }
        }
        else
            t_datos.setModel(ModeloTablaReporte(0, columnas));
        formatoTabla();
    }
    
    private boolean modifica()
    {
        Session session = HibernateUtil.getSessionFactory().openSession();
        try
        {
            session.beginTransaction();
            Object resp=session.createQuery("from Tipo obj where obj.tipoNombre='"+Nombre.getText()+"' and obj.tipoNombre!='"+ic+"'").uniqueResult();
            if (resp==null)
            {
                Query q;
                if(cb_ep.isSelected())
                    q = session.createQuery("update Tipo obj set obj.EPesado=1, obj.tipoNombre='"+Nombre.getText()+"'where obj.tipoNombre='"+ic+"'");
                else
                    q = session.createQuery("update Tipo obj set obj.EPesado=0, obj.tipoNombre='"+Nombre.getText()+"'where obj.tipoNombre='"+ic+"'");
                q.executeUpdate();
                session.beginTransaction().commit(); 
                JOptionPane.showMessageDialog(null, "Registro modificado");
                return true;
            }
            else
            {
                JOptionPane.showMessageDialog(null, "¡No se pueden guardar nombres duplicados!");  
                return false;
            }
        }
        catch (HibernateException he)
        {
            he.printStackTrace(); 
            System.out.println(he.hashCode());
            session.getTransaction().rollback();
            return false;
        }
        finally
        {        
            if(session.isOpen())
                session.close();
        }
    }
    
    private boolean elimina(String idTipo)
    {
        Session session = HibernateUtil.getSessionFactory().openSession();
        try
        {
            session.beginTransaction();
            actor = (Tipo)session.get(Tipo.class, idTipo);
            if(actor.getOrdens().isEmpty()==false|| 
            actor.getEjemplars().isEmpty()==false)
            {
                session.getTransaction().rollback();
                JOptionPane.showMessageDialog(null, "¡El Tipo esta en uso en una orden, no se puede eliminar!");
                return false;
            }
            else
            {
                session.delete(actor);
                session.getTransaction().commit();
                return true;
            }
        }catch(Exception e)
        {
            e.printStackTrace();
            session.getTransaction().rollback();
            return false;
        }
        finally
        {
            if(session.isOpen())
                session.close();
        }
    }

