                                            double suma=(((double)value)*((double)t_datos.getValueAt(row, 0)));
                                            double desc=((double)t_datos.getValueAt(row, 4))/100;
                                            double total=suma-(suma*desc);
                                            t_datos.setValueAt(total, row, 5);
