    public int OrdenFacturado(Orden or)
    {
        Session session = HibernateUtil.getSessionFactory().openSession();
        try
        {
            Orden a=(Orden)session.get(Orden.class, or.getIdOrden());
            Factura[] fac=(Factura[])a.getFacturas().toArray(new Factura[0]);
            for(int r=0; r<fac.length; r++)
            {
                if(fac[r].getEstadoFactura().compareTo("Pendiente")==0 || fac[r].getEstadoFactura().compareTo("Facturado")==0)
                    return fac[r].getIdFactura();
            }
            return -1;
        }catch(Exception e)
        {
            System.out.println(e);
            return -1;
        }
        finally
        {
            if(session!=null)
                if(session.isOpen())
                    session.close();
        }
    }
