    private void Selecciona2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Selecciona2ActionPerformed
        h=new Herramientas(usr, 0);
        h.session(sessionPrograma);
        Session session = HibernateUtil.getSessionFactory().openSession();
        usr = (Usuario)session.get(Usuario.class, usr.getIdUsuario());
        if(usr.getEditaReparacion()==true)
        {
            altaReparacion obj = new altaReparacion(new javax.swing.JFrame(), true, usr, sessionPrograma);
            Dimension d = Toolkit.getDefaultToolkit().getScreenSize();
            obj.setLocation((d.width/2)-(obj.getWidth()/2), (d.height/2)-(obj.getHeight()/2));
            obj.setVisible(true);
            borra_cajas();
            cajas(false, false, false);
            for(int ren=0; ren<t_datos.getRowCount(); ren++)
            {       
                if(t_datos.getValueAt(ren, 0).toString().compareTo("")!=0)
                    t_datos.setModel(ModeloTablaReporte(0, columnas));
            }
            buscaDato();
        }
        else
            JOptionPane.showMessageDialog(null, "¡Acceso denegado!");    
        if(session!=null)
            if(session.isOpen())
                session.close();
    }//GEN-LAST:event_Selecciona2ActionPerformed

