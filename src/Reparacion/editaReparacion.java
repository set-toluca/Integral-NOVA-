    private List<Object[]> executeHQLQuery(String hql) {
        Session session = HibernateUtil.getSessionFactory().openSession();
        try {
            session.beginTransaction();
            Query q = session.createQuery(hql);
            List resultList = q.list();
            session.getTransaction().commit();
            session.disconnect();
            return resultList;
        } catch (HibernateException he) {
            he.printStackTrace();
            List lista= null;
            return lista;
        }
        finally
        {
            if(session.isOpen())
            session.close();
        }
    }
    
    private void buscaDato()
    {
        String consulta="from Reparacion";
        List <Object[]> resultList=executeHQLQuery(consulta);
        if(resultList.size()>0)
        {
            t_datos.setModel(ModeloTablaReporte(resultList.size(), columnas));
            int i=0;
            for (Object o : resultList)
            {
                Reparacion actor = (Reparacion) o;
                model.setValueAt(actor.getIdReparacion(), i, 0);
                model.setValueAt(actor.getNombre(), i, 1);
                i++;
            }
        }else
            t_datos.setModel(ModeloTablaReporte(0, columnas));
        formatoTabla();
    }
    
    private boolean modifica()
    {
        Session session = HibernateUtil.getSessionFactory().openSession();
        try
        {
            session.beginTransaction();
            Object resp=session.createQuery("from Reparacion obj where obj.nombre='"+Nombre.getText()+"' and obj.idReparacion!="+IdReparacion.getText()).uniqueResult();
            if(resp==null)
            {
                Reparacion objeto = (Reparacion)session.get(Reparacion.class, Integer.parseInt(IdReparacion.getText()));
                objeto.setNombre(Nombre.getText().trim());  
                
                session.update(objeto);
                session.getTransaction().commit();
                cajas(false, false, false);
                return true;
            }
            else
            {
                JOptionPane.showMessageDialog(null, "¡No se pueden guardar nombres duplicados!");  
                return false;
            }
        }
        catch (HibernateException he)
        {
            he.printStackTrace(); 
            System.out.println(he.hashCode());
            session.getTransaction().rollback();
            return false;
        }
        finally
        {
            if(session.isOpen())
                session.close();
        }
    }
    
    private boolean elimina(String idReparacion)
    {
        Session session = HibernateUtil.getSessionFactory().openSession();
        try
        {
            session.beginTransaction();
            actor = (Reparacion)session.get(Reparacion.class, Integer.parseInt(idReparacion));
            
            if(actor.getOrdens().isEmpty()==false)
            {
                session.getTransaction().rollback();
                JOptionPane.showMessageDialog(null, "¡La Reparación esta en uso en una orden, no se puede eliminar!");
                return false;
            }
            else
            {
                session.delete(actor);
                session.getTransaction().commit();
                return true;
            }
        }catch(Exception e)
        {
            e.printStackTrace();
            session.getTransaction().rollback();
            return false;
        }
        finally
        {
            if(session.isOpen())
                session.close();
        }
    }
    
