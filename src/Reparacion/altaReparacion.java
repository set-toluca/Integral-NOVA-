    private void b_guardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_guardar1ActionPerformed
        b_cancelar.requestFocus();
        h=new Herramientas(user, 0);
        h.session(sessionPrograma);   
        t_nombre_reparacion.requestFocus();
        if(t_nombre_reparacion.getText().compareTo("")!=0)
        {
            if(consultaReparacion(t_nombre_reparacion.getText())==false)
            {
                Reparacion nuevoReparacion = new Reparacion();
                nuevoReparacion.setNombre(t_nombre_reparacion.getText());
                Integer respuesta=guardarReparacion(nuevoReparacion);
                if(respuesta==null)
                {
                    cajas(true, true);
                    b_guardar1.setEnabled(true);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Registro almacenado con la clave:  "+respuesta);
                    this.borra_cajas();
                    t_nombre_reparacion.requestFocus();
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "¡El nombre de la reparación ya existe!");
                this.borra_cajas();
                t_nombre_reparacion.requestFocus();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "¡Debe introducir el nombre de la reparación!");
            t_nombre_reparacion.requestFocus();
        }
    }//GEN-LAST:event_b_guardar1ActionPerformed

    private void b_cancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_cancelarActionPerformed
        int opt=JOptionPane.showConfirmDialog(this, "¡Los datos capturados se eliminarán!");
        if(opt==0)
            borra_cajas();
        t_nombre_reparacion.requestFocus(); 
    }//GEN-LAST:event_b_cancelarActionPerformed

