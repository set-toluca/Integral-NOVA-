                {
                    File archivo=new File("adjuntos/"+m.getIdCorreo()+"/"+m.getArchivo());
                    archivo.delete();
                    archivo=new File("adjuntos/"+m.getIdCorreo());
                    archivo.delete();
                }
                session.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "¬°Correo eliminado!");
