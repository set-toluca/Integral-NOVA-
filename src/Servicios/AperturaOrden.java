private Integer guardarOrden(Orden nueva)
{
    Session session = HibernateUtil.getSessionFactory().openSession();
    Integer ID = null;
    try 
    {
        session.beginTransaction().begin();
        Object resp=session.createSQLQuery("select if(max(id_orden) is null, ("+this.periodo+"*10000)+1, max(id_orden)+1) as id from orden obj where id_ciclo="+this.periodo).uniqueResult();
        if(this.c_siniestro.getItemCount()>0)
        {
            Reparacion re = (Reparacion)session.createCriteria(Reparacion.class).add(Restrictions.eq("nombre", this.c_siniestro.getSelectedItem().toString())).setMaxResults(1).uniqueResult();
            nueva.setReparacion(re);
        }
        BigInteger id_nuevo=(BigInteger) resp;
        nueva.setIdOrden(id_nuevo.intValue());
        //**********si hay foto la guardamos
        if(entro_foto==1)
        {
            if(guardaFoto(id_nuevo.intValue()))
            {
                Date fecha_orden = new Date();
                Foto img=new Foto(nueva, ""+id_nuevo.intValue()+".jpg", fecha_orden);
                nueva.addFoto(img);
                ID=(Integer) session.save(nueva);
                session.getTransaction().commit();
            }
        }
        else
        {
            ID=(Integer) session.save(nueva);
            session.getTransaction().commit();
        }
        File folder = new File(ruta+"ordenes/"+ID);
        folder.mkdirs();
        return ID;  
    } 
    catch (HibernateException he) 
    {
        he.printStackTrace();
        session.getTransaction().rollback();
        return null;   
    }
    finally
    {
        if(session!=null)
            if(session.isOpen())
                session.close(); 
    }
}

