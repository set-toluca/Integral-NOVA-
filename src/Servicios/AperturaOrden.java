    public void cargaEstatus()
    {
        Session session = HibernateUtil.getSessionFactory().openSession();
        try
        {
            Query qr=session.createSQLQuery("select estatus_nombre from estatus");
                qr.setResultTransformer(Criteria.ALIAS_TO_ENTITY_MAP);
                List resultList=(ArrayList)qr.list();
            if(resultList.size()>0)
            {
                c_estatus.removeAllItems();
                for (Object o : resultList)
                {
                    java.util.HashMap estatus=(java.util.HashMap)o;
                    c_estatus.addItem(estatus.get("estatus_nombre"));
                }
            }
            
            qr=session.createSQLQuery("select nombre from reparacion");
                qr.setResultTransformer(Criteria.ALIAS_TO_ENTITY_MAP);
                resultList=(ArrayList)qr.list();
            if(resultList.size()>0)
            {
                this.c_siniestro.removeAllItems();
                for (Object o : resultList)
                {
                    java.util.HashMap reparacion=(java.util.HashMap)o;
                    this.c_siniestro.addItem(reparacion.get("nombre"));
                }
            }
            resultList=null;
        }catch(Exception e)
        {
            System.out.println(e);
        }
        finally
        {
            if(session!=null)
                if(session.isOpen())
                    session.close();
        }
    }
    

