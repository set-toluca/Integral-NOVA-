    private List<Object[]> executeHQLQuery(String hql) {
        try {
            Session session = HibernateUtil.getSessionFactory().openSession();
            session.beginTransaction();
            Query q = session.createQuery(hql);
            List resultList = q.list();
            session.getTransaction().commit();
            session.disconnect();
            return resultList;
        } catch (HibernateException he) {
            he.printStackTrace();
            List lista= null;//new List(5);
            return lista;
        }
    }
    
    private void buscaDato()
    {
            String consulta="from Puestos obj";
            if(c_filtro.getSelectedItem().toString().compareTo("Clave")==0)
                consulta+=" where obj.idPuestos like '%" + t_busca.getText() +"%'";
            if(c_filtro.getSelectedItem().toString().compareTo("Nombre")==0)
                consulta+=" where obj.nombre like '%" + t_busca.getText() +"%'";
            if(c_filtro.getSelectedItem().toString().compareTo("Dirección")==0)
                consulta+=" where obj.descripcion like '%" + t_busca.getText() +"%'";
            List <Object[]> resultList=executeHQLQuery(consulta);
            if(resultList.size()>0)
            {
                t_datos1.setModel(ModeloTablaReporte(resultList.size(), columnas));
                int i=0;
                for (Object o : resultList) 
                {
                    Puestos actor = (Puestos) o;
                    model.setValueAt(actor.getIdPuestos(), i, 0);
                    model.setValueAt(actor.getNombre(), i, 1);
                    model.setValueAt(actor.getDescripcion(), i, 2);                       
                    i++;
                }
            }
            else
                t_datos1.setModel(ModeloTablaReporte(0, columnas));
        formatoTabla();
    }
