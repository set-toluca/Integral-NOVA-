    private List<Object[]> executeHQLQuery(String hql) 
    {
        try {
            Session session = HibernateUtil.getSessionFactory().openSession();
            session.beginTransaction();
            Query q = session.createQuery(hql);
            List resultList = q.list();
            session.getTransaction().commit();
            session.disconnect();
            return resultList;
        } catch (HibernateException he) {
            he.printStackTrace();
            List lista= null;
            return lista;
        }
    }
    private Integer guardarProveedor(Proveedor obj) 
    {
        Session session = HibernateUtil.getSessionFactory().openSession();
        Integer IdProveedor = null;
        try 
        {
            session.beginTransaction();
            IdProveedor=(Integer) session.save(obj);
            session.save(obj);
            session.getTransaction().commit();
            return null;
        } 
        catch (HibernateException he) 
        {
            he.printStackTrace();
            session.getTransaction().rollback();
            return null;
        }
        finally
        {
            session.close();
            return IdProveedor;
        }
    }
    public boolean consultaProveedor(String rfc)
    {
        List <Object[]> resultList=executeHQLQuery("from Proveedor obj where obj.rfc='"+rfc+"'");
        if(resultList.size()>0)
            return true;
        else
            return false;
    }
