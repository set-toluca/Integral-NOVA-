    private void b_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_guardarActionPerformed
        // TODO add your handling code here:
        h=new Herramientas(user, 0);
        h.session(sessionPrograma);
        b_cancelar.requestFocus();
        
        if(t_nombre.getText().compareTo("")!=0)
        {
            if(t_rfc.getText().compareTo("")!=0)
            {
                if(t_credito_plazo.getText().compareTo("")!=0)
                {
                    if(t_credito_descuento.getText().compareTo("")!=0)
                    {
                        if(t_tiempo_entrega.getText().compareTo("")!=0)
                        {
                            if(t_importe_compras.getText().compareTo("")!=0)
                            {                                
                                if(t_tipo.getItemCount()>0)
                                {
                                    if(consultaProveedor(t_rfc.getText())==false)
                                    {
                                        Proveedor registro = new Proveedor();

                                        if(t_nombre.getText().compareTo("")!=0)
                                            registro.setNombre(t_nombre.getText());
                                        if(t_direccion.getText().compareTo("")!=0)
                                            registro.setDireccion(t_direccion.getText());
                                        if(t_colonia.getText().compareTo("")!=0)
                                            registro.setColonia(t_colonia.getText());
                                        if(t_cp.getText().compareTo("")!=0)
                                            registro.setCp(t_cp.getText());
                                        if(t_poblacion.getText().compareTo("")!=0)
                                            registro.setPoblacion(t_poblacion.getText());
                                        registro.setEstado(t_estado.getSelectedItem().toString());
                                        if(t_rfc.getText().compareTo("")!=0)
                                        {
                                            t_rfc.setText(t_rfc.getText().trim());
                                            registro.setRfc(t_rfc.getText());
                                        }
                                        if(t_telefono1.getText().compareTo("")!=0)
                                            registro.setTel1(t_telefono1.getText());
                                        if(t_telefono2.getText().compareTo("")!=0)
                                            registro.setTel2(t_telefono2.getText());
                                        if(t_fax.getText().compareTo("")!=0)
                                            registro.setFax(t_fax.getText());                                        
                                        if(t_email.getText().compareTo("")!=0)
                                            registro.setEmail(t_email.getText());
                                        if(t_representante.getText().compareTo("")!=0)
                                            registro.setRepresentante(t_representante.getText());
                                        if(t_cuenta.getText().compareTo("")!=0)
                                            registro.setCta(t_cuenta.getText());
                                        if(t_credito_limite.getText().compareTo("")!=0)
                                            registro.setCredLimite(Float.parseFloat(t_credito_limite.getText()));
                                        if(t_credito_plazo.getText().compareTo("")!=0)
                                            registro.setCredPlazo(Integer.parseInt(t_credito_plazo.getText()));
                                        if(t_credito_descuento.getText().compareTo("")!=0)
                                            registro.setCredDescuento(Float.parseFloat(t_credito_descuento.getText()));
                                        if(t_tiempo_entrega.getText().compareTo("")!=0)
                                            registro.setTiempoEntrega(Integer.parseInt(t_tiempo_entrega.getText()));
                                        if(t_importe_compras.getText().compareTo("")!=0)
                                            registro.setImpCompras(Float.parseFloat(t_importe_compras.getText()));    
                                        if(t_giro.getText().compareTo("")!=0)
                                            registro.setGiro(t_giro.getText());
                                        if(t_notas.getText().compareTo("")!=0)
                                            registro.setNotas(t_notas.getText());      
                                        registro.setTipo(t_tipo.getSelectedIndex());
                                        if(checkhoja.isSelected())
                                            registro.setEspHojalateria(true);
                                        else
                                            registro.setEspHojalateria(false);
                                        if(espmeca.isSelected())
                                            registro.setEspMecanica(true);
                                        else
                                            registro.setEspMecanica(false);
                                        if(espsusp.isSelected())
                                            registro.setEspSuspension(true);
                                        else
                                            registro.setEspSuspension(false);                                 
                                        if(espelec.isSelected())
                                            registro.setEspElectricidad(true);
                                        else
                                            registro.setEspElectricidad(false);                                 
                                        Integer respuesta=guardarProveedor(registro);
                                        if(respuesta==null)
                                        {
                                            cajas(true, true, true, true, true, true, true, true, true, true, true, true, true, true, true, true, true, true, true, true, true);
                                            b_guardar.setEnabled(true);
                                        }
                                        else
                                        {
                                            JOptionPane.showMessageDialog(null, "Registro almacenado con la clave:  " +respuesta);
                                            borra_cajas();
                                            t_nombre.requestFocus();
                                        }
                                    }
                                    else
                                    {
                                        JOptionPane.showMessageDialog(null, "¡El RFC ya existe!");
                                        t_rfc.requestFocus();
                                    }
                                }
                                else
                                {
                                    JOptionPane.showMessageDialog(null, "¡Debe introducir el tipo!");
                                    t_tipo.requestFocus();
                                }
                            }
                            else
                            {
                                JOptionPane.showMessageDialog(null, "¡Debe introducir el imp de compras!");
                                t_importe_compras.requestFocus();
                            }
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(null, "¡Debe introducir el tiempo de entrega!");
                            t_tiempo_entrega.requestFocus();
                        }
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "¡Debe introducir el crédito de descuento!");
                        t_credito_descuento.requestFocus();
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "¡Debe introducir el crédito plazo!");
                    t_credito_plazo.requestFocus();
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "¡Debe introducir el RFC del proveedor!");
                t_rfc.requestFocus();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "¡Debe introducir el nombre del proveedor!");
            t_nombre.requestFocus();
        }
    }//GEN-LAST:event_b_guardarActionPerformed

